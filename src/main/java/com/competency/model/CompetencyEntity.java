package com.competency.model;

public class CompetencyEntity {
	private int competencyId;
	private String competency;
	private int categoryId;
	private String category;
	private int questionId;
	private int questionNbr;
	private int itemId;
	private String question;
	private String answerType;
	private int choiceId;
	private int choiceNbr;
	private String choice;
	private int weight;
	private String explanation;
	public int getCompetencyId() {
		return competencyId;
	}
	public void setCompetencyId(int competencyId) {
		this.competencyId = competencyId;
	}
	public String getCompetency() {
		return competency;
	}
	public void setCompetency(String competency) {
		this.competency = competency;
	}
	public int getCategoryId() {
		return categoryId;
	}
	public void setCategoryId(int categoryId) {
		this.categoryId = categoryId;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public int getQuestionId() {
		return questionId;
	}
	public void setQuestionId(int questionId) {
		this.questionId = questionId;
	}
	public int getQuestionNbr() {
		return questionNbr;
	}
	public void setQuestionNbr(int questionNbr) {
		this.questionNbr = questionNbr;
	}
	public int getItemId() {
		return itemId;
	}
	public void setItemId(int itemId) {
		this.itemId = itemId;
	}
	public String getQuestion() {
		return question;
	}
	public void setQuestion(String question) {
		this.question = question;
	}
	public String getAnswerType() {
		return answerType;
	}
	public void setAnswerType(String answerType) {
		this.answerType = answerType;
	}
	public int getChoiceId() {
		return choiceId;
	}
	public void setChoiceId(int choiceId) {
		this.choiceId = choiceId;
	}
	public int getChoiceNbr() {
		return choiceNbr;
	}
	public void setChoiceNbr(int choiceNbr) {
		this.choiceNbr = choiceNbr;
	}
	public String getChoice() {
		return choice;
	}
	public void setChoice(String choice) {
		this.choice = choice;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public String getExplanation() {
		return explanation;
	}
	public void setExplanation(String explanation) {
		this.explanation = explanation;
	}
	
}
