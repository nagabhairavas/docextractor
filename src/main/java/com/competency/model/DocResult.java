package com.competency.model;

public class DocResult {
	private int categoryId;
	private int termId;
	private String termDescr;
	private int competencyId;
	private String competencyDescr;
	public int getCategoryId() {
		return categoryId;
	}
	public void setCategoryId(int categoryId) {
		this.categoryId = categoryId;
	}
	public int getTermId() {
		return termId;
	}
	public void setTermId(int termId) {
		this.termId = termId;
	}
	public String getTermDescr() {
		return termDescr;
	}
	public void setTermDescr(String termDescr) {
		this.termDescr = termDescr;
	}
	public int getCompetencyId() {
		return competencyId;
	}
	public void setCompetencyId(int competencyId) {
		this.competencyId = competencyId;
	}
	public String getCompetencyDescr() {
		return competencyDescr;
	}
	public void setCompetencyDescr(String competencyDescr) {
		this.competencyDescr = competencyDescr;
	}
	
}
